def encryption(file, password):
    buffer_size = 512 * 1024
    pyAesCrypt.encryptFile(
        str(file),
        str(file) + ".crp",
        password,
        buffer_size
    )

    base = os.path.splitext(file)[0]
    print(f"[File '{base}' has been encrypted]")

    os.remove(file)

