def walikngByDirs(dir, password):

    for name in os.listdir(dir):
        path = os.path.join(dir, name)

        if (os.path.isfile(path)):
            try:
                decryption(path, password)
            except Exception as ex:
                print(ex)
        
        else:
            walikngByDirs(path, password)

