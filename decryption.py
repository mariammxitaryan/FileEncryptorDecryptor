def decryption(file, password):
    buffer_size = 512 * 1024
    pyAesCrypt.decryptFile(
        str(file),
        str(os.path.splitext(file)[0]),
        password,
        buffer_size
    )

    base = os.path.splitext(file)[0]
    print(f"[File '{base}' has been decrypted]")

    os.remove(file)

